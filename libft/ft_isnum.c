#include "libft.h"

int	ft_isnum(char *s1)
{
	int	ret;

	ret = 0;
	while (*s1 == ' ' && *s1 != '\0')
		s1++;
	while (*s1 != '\0')
	{
		if (*s1 < '0' || *s1 > '9')
			ret = 1;
		s1++;
	}
	return (ret);
}
